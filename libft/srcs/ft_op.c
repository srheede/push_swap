#include "../libft.h"

int		ft_op(int i, char c, int j)
{
	int k;

	if (c == '+')
		k = i + j;
	if (c == '-')
		k = i - j;
	if (c == '/')
		k = i / j;
	if (c == '*')
		k = i * j;
	if (c == '%')
		k = i % j;
	return (k);
}
