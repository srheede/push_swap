#include "../libft.h"

int		ft_sqrt(int n)
{
	double	x1;
	double	x2;

	x1 = (n * 1.0) / 2;
	x2 = (x1 + (n / x1)) / 2;
	while (ft_abs(x1 - x2) >= 0.0000001)
	{
		x1 = x2;
		x2 = (x1 + (n / x1)) / 2;
	}
	return (x2);
}
