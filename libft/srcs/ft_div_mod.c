#include "../libft.h"

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	if (a < b)
	{
		*mod = a;
	}
	else
	{
		while (a > b)
		{
			a -= b;
		}
		*mod = a;
	}
}
