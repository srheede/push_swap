#include "../libft.h"

int		ft_fibonacci(int i)
{
	if (i < 0 || i > 46)
		return (-1);
	if (i == 1)
		return (1);
	if (i == 0)
		return (0);
	return (ft_fibonacci(i - 1) + ft_fibonacci(i - 2));
}
